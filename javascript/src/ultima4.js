ultima4.main = (function() {
  function getMapTileAt(x, y) {
      return map.charCodeAt(y*256+x); //parseInt(map.substr(y*256*2 + x*2, 2), 16);
  }

  function drawTile(g, tile, x, y) {
    g.drawImage(tiles, (tile%16)*16, Math.floor(tile/16)*16, 16, 16, x, y, 32, 32);
  }

  function drawViewport(g, mapX, mapY) {
    var ox = 16 + 5*32;
    var oy = 16 + 5*32;
    for (var y=-5; y<=5; y++) {
      for (var x=-5; x<=5; x++) {
        drawTile(g, getMapTileAt(mapX+x,mapY+y), ox + x*32, oy + y*32);
      }
    }
    drawTile(g, 31, ox, oy);
  }

  function repaint() {
    var g = canvas.getContext("2d");
    g.fillStyle = '#fff';
    g.strokeStyle = '#000';
    g.fillRect(0, 0, canvas.width, canvas.height);
    g.strokeRect(0, 0, canvas.width, canvas.height);
    drawViewport(g, state.x, state.y);
  }

  function canMoveTo(mapX, mapY) {
    var ret = true;
    var tile = getMapTileAt(mapX, mapY);
    switch(tile) {
    case tileType.ocean:
    case tileType.river:
    case tileType.mountain:
      ret = false;
    }
    return ret;
  }

  function keyDown(e) {
    switch(e.keyCode) {
    case dir.up:
      if (canMoveTo(state.x, state.y - 1)) {
        state.y--;
      }
      break;
    case dir.left:
      if (canMoveTo(state.x - 1, state.y)) {
        state.x--;
      }
      break;
    case dir.down:
      if (canMoveTo(state.x, state.y + 1)) {
	state.y++;
      }
      break;
    case dir.right:
      if (canMoveTo(state.x + 1, state.y)) {
	state.x++;
      }
      break;
    }
    repaint();
  }

  function createCanvas() {
    var canvas = document.createElement("canvas");
    canvas.width = 600;
    canvas.height = 480;
    return canvas;
  }

  function createTiles() {
    var tiles = new Image();
    tiles.src = ultima4.gameData.tiles;
    tiles.onload = onFinishedLoading;
    return tiles;
  }

  function onFinishedLoading() {
    repaint();
  }

  var dir = {
    up: 38,
    left: 37,
    down: 40,
    right: 39
  };

  var tileType = {
    deepOcean: 0,
    ocean: 1,
    river: 2,
    grass: 4,
    bushes: 5,
    forrest: 6,
    hill: 7,
    mountain: 8
  };

  var state = {
    x: 86,
    y: 108
  };

  var map = ultima4.gameData.worldMap;
  var tiles = createTiles();
  var canvas = createCanvas();

  document.write("<body>");
  document.onkeydown = keyDown;
  document.body.appendChild(canvas);
}());